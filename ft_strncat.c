#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	char	*str;

	str = dest;
	while (*str)
		str++;
	while (*src && n-- > 0)
	{
		*str = *src;
		str++;
		src++;
	}
	*str = '\0';
	return (dest);
}
